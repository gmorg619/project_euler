# The prime factors of 13195 are 5, 7, 13 and 29.
#
# What is the largest prime factor of the number 600851475143?
# ------------------------------------------------------------------------------

num = 600851475143
max_prime = 0
i = 2

while i <= num
  if num % i == 0
    num /=i
    if i > max_prime
      max_prime = i
    end
  end
  i +=1
end

puts max_prime

